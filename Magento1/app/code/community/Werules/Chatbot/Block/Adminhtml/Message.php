class Werules_Chatbot_Block_Adminhtml_Message extends Mage_Adminhtml_Block_Widget_Grid_Container
{
    /**
     * constructor
     *
     * @access public
     * @return void
     */
    public function __construct()
    {
        $this->_controller = 'adminhtml_message';
        $this->_blockGroup = 'werules_chatbot';
        parent::__construct();
        $this->_headerText = Mage::helper('werules_chatbot')->__('Message');
        $this->_updateButton('add', 'label', Mage::helper('werules_chatbot')->__('Add Message'));

    }
}
